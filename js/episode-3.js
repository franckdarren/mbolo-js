// Demander de saisir le premier nombre
let nombre1 = prompt("Veuillez saisir le premier nombre");

// Demander de saisir le deuxième nombre
let nombre2 = prompt("Veuillez saisir le deuxième nombre");

// Afficher la somme des deux nombres en alerte
let somme = parseInt(nombre1) + parseInt(nombre2);
alert("La somme des deux nombres est : " + somme);